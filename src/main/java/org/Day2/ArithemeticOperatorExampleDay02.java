package org.Day2;

public class ArithemeticOperatorExampleDay02 {
    public static void main(String[] args) {
        // + (addition) // - (subsctraction) // * (multiply)
        // / (division) (divinded)
        // 10/02 =5 - 10/3 - 3 -> 3.33
        int y=10/3;
        System.out.println(y);
        float x=10f/3;
        System.out.println(x);
        double z=10.00/3;
        System.out.println(z);
        // % Modulo -> remainder
        // 10/3 -> 3 -1
        double rem=10%3;
        System.out.println(rem);

        double rem1=100%7;
        System.out.println(rem1);
    }
}
